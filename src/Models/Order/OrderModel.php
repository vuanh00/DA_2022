<?php

namespace SRC\Models\Order;

use SRC\Core\Model;
use SRC\Models\OrderDetail\OrderDetailResourceModel;

/** 
 * A Order class
 *
 * @param  table orders
 * @param  id id
 * 
 */
class OrderModel extends Model
{
    private $id;
    private $customer_id;
    private $date;
    private $status;


    /**
     * Get the value of id
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set the value of id
     *
     * @return  self
     */
    public function setId($id)
    {
        $this->id = $id;

        return $this;
    }

    /**
     * Get the value of customer_id
     */
    public function getCustomer_id()
    {
        return $this->customer_id;
    }

    /**
     * Set the value of customer_id
     *
     * @return  self
     */
    public function setCustomer_id($customer_id)
    {
        $this->customer_id = $customer_id;

        return $this;
    }

    /**
     * Get the value of date
     */
    public function getDate()
    {
        return $this->date;
    }

    /**
     * Set the value of date
     *
     * @return  self
     */
    public function setDate($date)
    {
        $this->date = $date;

        return $this;
    }

    /**
     * Get the value of price
     */
    public function getSubtotal()
    {
        $orderDetailResourceModel =  new OrderDetailResourceModel();
        return $orderDetailResourceModel->where('order_id', $this->id)
            ->select("sum(orderdetails.price) as sum_price")
            ->get()->sum_price;
    }

    /**
     * Set the value of price
     *
     * @return  self
     */
    public function setPrice($price)
    {
        $this->price = $price;

        return $this;
    }

    /**
     * Get the value of status
     */
    public function getStatus()
    {
        return $this->status;
    }

    /**
     * Set the value of status
     *
     * @return  self
     */
    public function setStatus($status)
    {
        $this->status = $status;

        return $this;
    }
}
